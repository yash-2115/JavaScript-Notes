console.log("Assignment Operators");
let a = 10;

console.log("a = 10 --> ", a); // x = y
console.log("a += 5 --> ", a+=5); // same x = x + y
console.log("a -= 5 --> ", a-=5); // same x = x - y
console.log("a *= 5 --> ", a*=5); // same x = x * y
console.log("a /= 5 --> ", a/=5); // same x = x / y
console.log("a %= 5 --> ", a%=5); // same x = x % y
console.log("a **= 5 --> ", a**=5); // same x = x ** y